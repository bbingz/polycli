#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { build } from "esbuild";
import {
  COMMAND_SURFACE_VERSION,
  listCommandDefinitions,
} from "../plugins/polycli/scripts/lib/command-registry.mjs";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(HERE, "..");
const ENTRY = path.join(REPO_ROOT, "plugins/polycli/scripts/polycli-companion.mjs");
const TERMINAL_PACKAGE = JSON.parse(
  fs.readFileSync(path.join(REPO_ROOT, "packages/polycli-terminal/package.json"), "utf8"),
);
const TERMINAL_SURFACE_TARGET = path.join(
  REPO_ROOT,
  "packages/polycli-terminal/lib/command-surface.generated.mjs",
);
const TARGETS = [
  "plugins/polycli/scripts/polycli-companion.bundle.mjs",
  "plugins/polycli-codex/scripts/polycli-companion.bundle.mjs",
  "plugins/polycli-copilot/scripts/polycli-companion.bundle.mjs",
  "plugins/polycli-opencode/scripts/polycli-companion.bundle.mjs",
  "packages/polycli-terminal/bin/polycli-companion.bundle.mjs",
];

export function renderTerminalCommandSurface() {
  const definitions = listCommandDefinitions({ hostSurface: "terminal" });
  return `// Generated by scripts/build-plugin-bundles.mjs. Do not edit by hand.\n\n`
    + `export const COMMAND_SURFACE_VERSION = ${JSON.stringify(COMMAND_SURFACE_VERSION)};\n\n`
    + `export const TERMINAL_COMMAND_DEFINITIONS = deepFreeze(${JSON.stringify(definitions, null, 2)});\n\n`
    + `function deepFreeze(value) {\n`
    + `  if (!value || typeof value !== "object" || Object.isFrozen(value)) return value;\n`
    + `  for (const child of Object.values(value)) deepFreeze(child);\n`
    + `  return Object.freeze(value);\n`
    + `}\n\n`
    + `export function getTerminalCommandDefinition(path) {\n`
    + `  const key = Array.isArray(path) ? path.join(".") : String(path);\n`
    + `  return TERMINAL_COMMAND_DEFINITIONS.find((entry) => entry.path.join(".") === key) ?? null;\n`
    + `}\n`;
}

function generateTerminalCommandSurface() {
  fs.writeFileSync(TERMINAL_SURFACE_TARGET, renderTerminalCommandSurface(), "utf8");
}

async function main() {
  generateTerminalCommandSurface();
  for (const relativeTarget of TARGETS) {
    await build({
      entryPoints: [ENTRY],
      outfile: path.join(REPO_ROOT, relativeTarget),
      bundle: true,
      format: "esm",
      platform: "node",
      target: "node20",
      legalComments: "none",
      define: {
        __POLYCLI_VERSION__: JSON.stringify(TERMINAL_PACKAGE.version),
      },
    });
  }
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  main().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
